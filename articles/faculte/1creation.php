<p>
    En 1836, sur proposition de Jean-Baptiste Thorn, deuxième gouverneur du Hainaut de la Belgique indépendante, le
    conseil Provincial du Hainaut reconnaît la nécessité pour la Province de promouvoir la formation d’ingénieurs aptes
    à exercer les diverses professions industrielles de l’époque. Nos houillères, hauts fourneaux, laminoirs, verreries,
    faïenceries, raffineries et sucreries se développent à un rythme soutenu et les bassins industriels du Hainaut sont
    en pleine transformation. En sa séance du 21 octobre 1836, le Conseil Provincial prend la décision de créer à Mons
    une « Ecole Provinciale des Mines du Hainaut ». L’arrêté royal du 27 septembre 1837 la place sous le contrôle de la
    Députation Permanente et, le 1er novembre de la même année, les cours s’ouvrent dans un local offert par la Ville de Mons.
    Les crédits annuels sont alloués par la Province de Hainaut et par la Ville de Mons. Les premiers cours de l’<b>École
        Provinciale des Mines du Hainaut</b> (1837-1838) auront lieu le premier lundi de novembre 1837
</p><p>
    Suite à l'annonce dans la presse de la vacance des différentes chaires, Théophile Guibal et Barthélémi-Adolphe Devillez,
    deux jeunes et brillants ingénieurs issus de l'Ecole Centrale de Paris, postulent et sont choisis. Ils sont
    conjointement chargés d'organiser le programme des études.
</p><p>
    Guibal avait alors 23 ans et Devillez 24 ans: M. Thorn avait, à raison comme la suite le prouva, fait
    confiance à la jeunesse. Devillez assure la direction de l'Ecole, charge qu'il assumera pendant 51 ans.
</p><p>
    Avec des bases aussi rudimentaires, il ne pouvait être question de former des ingénieurs universitaires : l'École se
    bornait à instruire de bons "contremaîtres". La formation ne durait alors que deux ans. L'École était d'ailleurs jumelée avec l'École
    Industrielle de Charleroi dont les ambitions n'étaient pas plus élevées. Ce que l'on voulait avant tout, c'était une
    formation pratique ou orientée vers la pratique, vers le rendement immédiat.
</p><p>
    Si l'Ecole avait été créée, on se doute bien que c'est grâce au sentiment qu'elle serait rentable pour l'industrie.
    Du moment qu'elle ne rencontrait qu'un succès mitigé (entre 10 et 40 étudiants en fonction des années),
    un conseiller provincial fit la proposition de sa suppression au cours de la séance du 5 juillet 1844. L'énergie du Gouverneur Thorn
    et de Devillez évita le naufrage: la proposition fut repoussée et le programme fut même développé au-delà de ce qu'avait voulu son promoteur.
</p>
