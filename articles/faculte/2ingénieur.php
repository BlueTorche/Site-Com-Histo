<p>
En 1845, le Conseil Provincial approuve un projet de réorganisation qui augmente la durée des études
et crée une section commerciale. L'institution porte alors le nom d'"<b>École de Commerce, d'Industrie et
des Mines du Hainaut</b>". Elle est officiellement reconnue sous ce titre par l'État et bénéficie de subsides
gouvernementaux. Sa population dépasse alors les 40 étudiants, mais retombe à une vingtaine en 1851. Elle décide alors
de se séparer de l'École Industrielle de Charleroi. Cette séparation aura pour conséquence une explosion du nombre
d'étudiants, atteignant 78 en 1856 !
</p><p>
L'accroissement important des progrès scientifiques et industriels et la rapidité de l'essor économique
exige ensuite un développement de l'étendue de la formation des ingénieurs et une plus grande
spécialisation. Outre les diplômes initiaux de mines et de métallurgiste, l'École
organise successivement ceux de chimie en 1853 et de mécanique en 1862, celui des chemins de fer
en 1874 et le diplôme d'ingénieur électricien dès 1887. Les extensions constantes des enseignements
obligent, en 1876, à augmenter la durée des études. L'École marque cette évolution en complétant
son titre par celui de "<b>Faculté Polytechnique du Hainaut</b>" et ses étudiants deviennent enfin des ingénieurs
une fois leur diplôme obtenu.
</p><p>
Guibal meurt le 16 septembre 1888. En 1889, Devillez se retire de son poste de Directeur, après 51 années à diriger l'École.
Il rejoindra son compagnon Guibal le 9 février 1891.</p>